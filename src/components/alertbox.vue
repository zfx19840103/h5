<template>
    <div>
        <el-dialog 
            class="alertBox"
            @open="openCallback"
            :show-close = false
            :modal = false
            :before-close="handleClose"
            :visible.sync="alertBox" width="40%">
            <div class="content"><slot></slot></div>
        </el-dialog>
    </div>
</template>

<script>

export default {
    data: function() {
        return {
            closeTime: 1500 //定时1秒关闭
        };
    },
    props: ["alertBox"],
    created() {},
    methods: {
        handleClose() {
            this.$emit('close')
        },
        openCallback() {
            let that =this;
            setTimeout(()=> {
                that.$emit('close')
            }, that.closeTime)
        }
    }
};
</script>

<style scoped>
    .content {
        font-size:14px;
        font-family:PingFangSC-Regular,PingFang SC;
        font-weight:400;
        color:rgba(255,255,255,1);
        padding: 0 10px;
    }
</style>

